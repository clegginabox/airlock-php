const ALERT_CLASSES = {
    info: 'alert alert-info',
    success: 'alert alert-success',
    warning: 'alert alert-warning',
    error: 'alert alert-error',
};

export function alertHtml(type, icon, text) {
    return '<div class="' + (ALERT_CLASSES[type] || 'alert') + ' status-alert mt-4"><span>' + icon + '</span><span>' + text + '</span></div>';
}
